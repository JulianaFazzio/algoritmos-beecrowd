# Lê um número inteiro da entrada padrão e armazena na variável A
A = int(input())

# Lê outro número inteiro e armazena na variável B
B = int(input())

# Calcula a soma de A e B e armazena o resultado na variável SOMA
SOMA = A + B

# Exibe o resultado da soma no formato exigido: "SOMA = <valor>"
print("SOMA = {}".format(SOMA))
