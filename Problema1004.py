# Lê um número inteiro da entrada padrão e armazena na variável A
A = int(input())

# Lê outro número inteiro e armazena na variável B
B = int(input())

# Calcula o produto (multiplicação) de A e B e armazena em PROD
PROD = A * B

# Exibe o resultado do produto no formato exigido: "PROD = <valor>"
print("PROD = {}".format(PROD))
